package com.Exception;

public class WasherAlreadyExistException extends RuntimeException {

	public WasherAlreadyExistException() {
		super();

	}

	public WasherAlreadyExistException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public WasherAlreadyExistException(String message, Throwable cause) {
		super(message, cause);

	}

	public WasherAlreadyExistException(String message) {
		super(message);

	}

	public WasherAlreadyExistException(Throwable cause) {
		super(cause);

	}

}

