package com.Exception;

public class WashPackAlreadyExistException extends RuntimeException {

	public WashPackAlreadyExistException() {
		super();

	}

	public WashPackAlreadyExistException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public WashPackAlreadyExistException(String message, Throwable cause) {
		super(message, cause);

	}

	public WashPackAlreadyExistException(String message) {
		super(message);

	}

	public WashPackAlreadyExistException(Throwable cause) {
		super(cause);

	}



}

