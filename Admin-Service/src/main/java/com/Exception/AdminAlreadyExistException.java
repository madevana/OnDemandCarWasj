package com.Exception;

public class AdminAlreadyExistException extends RuntimeException {

	public AdminAlreadyExistException() {
		super();

	}

	public AdminAlreadyExistException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public AdminAlreadyExistException(String message, Throwable cause) {
		super(message, cause);

	}

	public AdminAlreadyExistException(String message) {
		super(message);

	}

	public AdminAlreadyExistException(Throwable cause) {
		super(cause);

	}



}
