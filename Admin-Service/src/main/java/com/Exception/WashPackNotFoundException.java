package com.Exception;

public class WashPackNotFoundException extends RuntimeException {

	public WashPackNotFoundException() {
		super();

	}

	public WashPackNotFoundException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public WashPackNotFoundException(String message, Throwable cause) {
		super(message, cause);

	}

	public WashPackNotFoundException(String message) {
		super(message);

	}

	public WashPackNotFoundException(Throwable cause) {
		super(cause);

	}

}


